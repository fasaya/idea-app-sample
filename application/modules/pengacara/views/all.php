              <p>Release by way of Appearance Notice or Promise to Appear is typically reserved for detained people with little or no prior criminal history, and for relatively low level criminal charges. Release of this type is in the discretion of the police, and does not require a formal Bail hearing. The release conditions associated with these documents are very minimal.</p>
              <br />
              <h6 class="text-stone roboto-slab">Recognizance</h6>
              <p>Release by way of a Recognizance is reserved for cases where the charges are serious, the accused is a flight risk, there is a significant risk that further offences will be committed while on release, and/or there is a significant criminal history. In such circumstances, the police either do not have discretion to release the person themselves, or are bound by policy to conduct a bail hearing.</p>
